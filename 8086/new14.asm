.MODEL SMALL
.STACK 100
.DATA

		STR1 DB 'Enter a digit: $'
		NUM1 DB ?
		NL DB 0AH,0DH,'$'


.CODE
MAIN PROC

		MOV AX,@DATA
		MOV DS,AX
		
		MOV AH,09H
		LEA DX,STR1
		INT 21H
		
		MOV AH,01H
		INT 21H
		SUB AL,30H
		MUL AL
		MOV BX,AX
		
		MOV AH,09H
		LEA DX,NL
		INT 21H

		MOV AH,02H
		MOV DL,BL
		ADD DL,30H
		INT 21H
		
		MOV AX,4C00H
		INT 21H

MAIN ENDP
END MAIN