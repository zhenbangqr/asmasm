.MODEL SMALL
.STACK 100
.DATA
		CHAR DB ?
		STR1 DB 'Enter the grade you want: $'
		STR2 DB 'I want to get '
		NUM DB 8
		NL DB 0AH,0DH,'$'


.CODE
MAIN PROC

		MOV AX,@DATA
		MOV DS,AX

		MOV AH,09H
		LEA DX,STR1
		INT 21H
		
		MOV AH,01H
		INT 21H
		MOV CHAR,AL
		
		MOV AH,09H
		LEA DX,NL
		INT 21H
		
		MOV AH,09H
		LEA DX,STR2
		INT 21H
		
		MOV AH,02H
		MOV BL,NUM
		ADD BL,30H
		MOV DL,BL
		INT 21H
		
		MOV AH,02H
		MOV BL,CHAR
		MOV DL,BL
		INT 21H
		
		MOV AX,4C00H
		INT 21H

MAIN ENDP
END MAIN