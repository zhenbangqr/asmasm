.MODEL SMALL
.STACK 100
.DATA
		NUM DB ?
		STR1 DB 'Enter a digit: $'
		NL DB 0AH,0DH,'$'


.CODE
MAIN PROC

		MOV AX,@DATA
		MOV DS,AX

		MOV AH,09H
		LEA DX,STR1
		INT 21H
		
		MOV AH,01H
		INT 21H
		
		MOV AH,09H
		LEA DX,NL
		INT 21H
		
		MOV AH,02H
		MOV AL,NUM
		MOV BL,AL
		ADD BL,2
		ADD BL,30H
		MOV DL,BL
		INT 21H
		
		MOV AX,4C00H
		INT 21H

MAIN ENDP
END MAIN