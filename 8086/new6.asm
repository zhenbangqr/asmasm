.MODEL SMALL
.STACK 100
.DATA
		STR1 DB "*****$"
		STR2 DB "*    *$"
		NL DB 0AH,0DH,'$'


.CODE
MAIN PROC

		MOV AX,@DATA
		MOV DS,AX
			
		MOV AH,09H
		LEA DX,STR1
		INT 21H
		
		MOV AH,09H
		LEA DX,NL
		INT 21H
		
		MOV AH,09H
		LEA DX,STR2
		INT 21H
		
		MOV AH,09H
		LEA DX,NL
		INT 21H
		
		MOV AH,09H
		LEA DX,STR2
		INT 21H
		
		MOV AH,09H
		LEA DX,NL
		INT 21H
		
		MOV AH,09H
		LEA DX,STR1
		INT 21H

		MOV AX,4C00H
		INT 21H

MAIN ENDP
END MAIN