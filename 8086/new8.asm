.MODEL SMALL
.STACK 100
.DATA
		NUM1 DB 3
		NUM2 DB 5
		NL DB 0AH,0DH,'$'
		STR1 DB " plus $"
		STR2 DB " equals to $"

.CODE
MAIN PROC

		MOV AX,@DATA
		MOV DS,AX
		
		MOV AH,02H
		MOV BL,NUM1
		ADD BL,30H
		MOV DL,BL

		INT 21H
		
		MOV AH,09H
		LEA DX,STR1
		INT 21H
		
		MOV AH,02H
		MOV BL,NUM2
		ADD BL,30H
		MOV DL,BL
		INT 21H
		
		MOV AH,09H
		LEA DX,STR2
		INT 21H
		
		MOV AH,02H
		MOV BL,NUM1
		ADD BL,NUM2
		ADD BL,30H
		MOV DL,BL
		INT 21H

		MOV AX,4C00H
		INT 21H

MAIN ENDP
END MAIN