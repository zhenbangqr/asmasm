.MODEL SMALL
.STACK 100
.DATA
		CH1 DB 'A'

.CODE
MAIN PROC

		MOV AX,@DATA
		MOV DS,AX
		
		MOV AH,02H
		MOV BL,CH1
		MOV DL,BL
		INT 21H
		
		MOV AH,02H
		MOV BL,','
		MOV DL,BL
		INT 21H
		
		MOV AH,02H
		MOV BL,CH1
		ADD BL,32
		MOV DL,BL
		INT 21H
		
		MOV AX,4C00H
		INT 21H

MAIN ENDP
END MAIN